# Estrutura de repetição controlada (quando sabe a quantidade de repetição)

# range define a faixa(quantidade repetições)

# Vai de 0 até menor que 10 (valor de parada)
for variavel in range(10):
    print(variavel)

print('--------------------------')

# Vai de 1 até 5 (menor que 6) 
for variavel in range(1, 6):
    print(variavel)

print('--------------------------')

# valor de incio 1, valor de parada 10, passo 2
for variavel in range(0, 10, 2):
    print(variavel)